function mixColumns(state) {
  for (var c = 0; c < 4; c++) {
    var a = state[0][c], b = state[1][c], d = state[2][c], e = state[3][c];
    state[0][c] = GF_MUL2[a] ^ GF_MUL2[b] ^ b ^ d ^ e;
    state[1][c] = GF_MUL2[b] ^ GF_MUL2[d] ^ d ^ e ^ a;
    state[2][c] = GF_MUL2[d] ^ GF_MUL2[e] ^ e ^ a ^ b;
    state[3][c] = GF_MUL2[e] ^ GF_MUL2[a] ^ a ^ b ^ d;
  }
}

// AES 加密单个 16 字节块
function aesEncryptBlock(block, w) {
  var state = [[],[],[],[]];
  for (var r = 0; r < 4; r++)
    for (var c = 0; c < 4; c++)
      state[r][c] = block[r + 4*c];

  addRoundKey(state, w, 0);
  for (var round = 1; round < 14; round++) {
    subBytes(state); shiftRows(state); mixColumns(state); addRoundKey(state, w, round);
  }
  subBytes(state); shiftRows(state); addRoundKey(state, w, 14);

  var out = new Array(16);
  for (var r = 0; r < 4; r++)
    for (var c = 0; c < 4; c++)
      out[r + 4*c] = state[r][c];
  return out;
}

// ==================== CBC 模式 + PKCS7 填充 ====================

// PKCS7 填充
function pkcs7Pad(data) {
  var blockSize = 16;
  var padLen = blockSize - (data.length % blockSize);
  var padded = data.slice();
  for (var i = 0; i < padLen; i++) padded.push(padLen);
  return padded;
}

// XOR 两个 16 字节数组
function xorBlocks(a, b) {
  var out = new Array(16);
  for (var i = 0; i < 16; i++) out[i] = a[i] ^ b[i];
  return out;
}

// AES-CBC 加密
function aesCbcEncrypt(keyBytes, iv, plainBytes) {
  var w = keyExpansion(keyBytes);
  var padded = pkcs7Pad(plainBytes);
  var ciphertext = [];
  var prevBlock = iv.slice();
  for (var i = 0; i < padded.length; i += 16) {
    var block = padded.slice(i, i + 16);
    var xored = xorBlocks(block, prevBlock);
    var encBlock = aesEncryptBlock(xored, w);
    for (var j = 0; j < 16; j++) ciphertext.push(encBlock[j]);
    prevBlock = encBlock;
  }
  return ciphertext;
}

// ==================== 随机数 & Base64 ====================

// 随机字节（兼容微信小程序：优先 crypto，回退 Math.random）
function randomBytes(len) {
  var arr = new Array(len);
  try {
    // 微信小程序中 global.crypto 可能不可用
    if (typeof wx !== 'undefined' && wx.getRandomValues) {
      var buf = wx.getRandomValues(new Uint8Array(len));
      for (var i = 0; i < len; i++) arr[i] = buf[i];
      return arr;
    }
  } catch(e) {}
  // 回退方案
  for (var i = 0; i < len; i++) {
    arr[i] = Math.floor(Math.random() * 256);
  }
  return arr;
}

// Base64 编码
var BASE64_CHARS = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/';
function base64Encode(bytes) {
  var result = '';
  var len = bytes.length;
  for (var i = 0; i < len; i += 3) {
    var b1 = bytes[i];
    var b2 = i + 1 < len ? bytes[i + 1] : 0;
    var b3 = i + 2 < len ? bytes[i + 2] : 0;
    result += BASE64_CHARS.charAt(b1 >> 2);
    result += BASE64_CHARS.charAt(((b1 & 3) << 4) | (b2 >> 4));
    result += i + 1 < len ? BASE64_CHARS.charAt(((b2 & 15) << 2) | (b3 >> 6)) : '=';
    result += i + 2 < len ? BASE64_CHARS.charAt(b3 & 63) : '=';
  }
  return result;
}

// ==================== 公开接口 ====================

// 字符串转字节数组
function stringToBytes(str) {
  var bytes = [];
  for (var i = 0; i < str.length; i++) {
    var code = str.charCodeAt(i);
    if (code < 128) {
      bytes.push(code);
    } else if (code < 2048) {
      bytes.push(192 | (code >> 6));
      bytes.push(128 | (code & 63));
    } else {
      bytes.push(224 | (code >> 12));
      bytes.push(128 | ((code >> 6) & 63));
      bytes.push(128 | (code & 63));
    }
  }
  return bytes;
}

/**
 * AES-256-CBC 加密
 * @param {string} plaintext - 明文
 * @returns {string} Base64 编码的密文（IV + 密文），与后端 AesUtil.decrypt() 兼容
 */
function encrypt(plaintext) {
  if (!plaintext) return '';
  var keyBytes = stringToBytes(AES_KEY);
  // 确保密钥正好 32 字节
  while (keyBytes.length < 32) keyBytes.push(0);
  if (keyBytes.length > 32) keyBytes = keyBytes.slice(0, 32);

  var iv = randomBytes(16);
  var plainBytes = stringToBytes(plaintext);
  var cipherBytes = aesCbcEncrypt(keyBytes, iv, plainBytes);

  // 拼接 IV + 密文，然后 Base64 编码
  var combined = iv.concat(cipherBytes);
  return base64Encode(combined);
}

