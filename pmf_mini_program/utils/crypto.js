// Rcon
var RCON = [0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36];

// 密钥扩展
function keyExpansion(key) {
  var Nk = 8; // AES-256: 8 words
  var Nr = 14; // AES-256: 14 rounds
  var Nb = 4;
  var w = new Array(Nb * (Nr + 1));
  for (var i = 0; i < Nk; i++) {
    w[i] = (key[4*i] << 24) | (key[4*i+1] << 16) | (key[4*i+2] << 8) | key[4*i+3];
  }
  for (var i = Nk; i < Nb * (Nr + 1); i++) {
    var temp = w[i-1];
    if (i % Nk === 0) {
      temp = subWord(rotWord(temp)) ^ (RCON[Math.floor(i/Nk)-1] << 24);
    } else if (Nk > 6 && i % Nk === 4) {
      temp = subWord(temp);
    }
    w[i] = w[i-Nk] ^ temp;
  }
  return w;
}

function rotWord(w) { return (w << 8) | (w >>> 24); }

function subWord(w) {
  return (SBOX[(w >>> 24) & 0xff] << 24) |
         (SBOX[(w >>> 16) & 0xff] << 16) |
         (SBOX[(w >>> 8) & 0xff] << 8) |
         (SBOX[w & 0xff]);
}

// 轮操作
function addRoundKey(state, w, round) {
  for (var c = 0; c < 4; c++) {
    state[0][c] ^= (w[round*4+c] >>> 24) & 0xff;
    state[1][c] ^= (w[round*4+c] >>> 16) & 0xff;
    state[2][c] ^= (w[round*4+c] >>> 8) & 0xff;
    state[3][c] ^= w[round*4+c] & 0xff;
  }
}

function subBytes(state) {
  for (var r = 0; r < 4; r++)
    for (var c = 0; c < 4; c++)
      state[r][c] = SBOX[state[r][c]];
}

function shiftRows(state) {
  var t = state[1][0]; state[1][0] = state[1][1]; state[1][1] = state[1][2]; state[1][2] = state[1][3]; state[1][3] = t;
  t = state[2][0]; state[2][0] = state[2][2]; state[2][2] = t;
  t = state[2][1]; state[2][1] = state[2][3]; state[2][3] = t;
  t = state[3][0]; state[3][0] = state[3][3]; state[3][3] = state[3][2]; state[3][2] = state[3][1]; state[3][1] = t;
}

